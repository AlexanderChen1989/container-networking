#!/bin/bash -e 

CON1="con1"
CON2="con2"
IP1="10.1.1.1"
IP2="10.1.1.2"

echo "========================================================================"
echo "Namespaces"
echo "========================================================================"
ip netns list
echo

echo "========================================================================"
echo "host - ifconfig"
echo "========================================================================"
sudo ifconfig
echo "========================================================================"
echo "$CON1 namespace - ifconfig"
echo "========================================================================"
sudo ip netns exec $CON1 ifconfig
echo "========================================================================"
echo "$CON2 namespace - ifconfig"
echo "========================================================================"
sudo ip netns exec $CON2 ifconfig

echo "========================================================================"
echo "host - route table"
echo "========================================================================"
sudo route
echo
echo "========================================================================"
echo "$CON1 namespace - route table"
echo "========================================================================"
sudo ip netns exec $CON1 route
echo
echo "========================================================================"
echo "$CON2 namespace - route table"
echo "========================================================================"
sudo ip netns exec $CON2 route
echo

