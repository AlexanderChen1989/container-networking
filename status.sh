#!/bin/bash -e 

CON1="con1"
CON2="con2"

echo "========================================================================"
echo "Namespaces"
echo "========================================================================"
ip netns list
echo

echo "========================================================================"
echo "node - ip addr"
echo "========================================================================"
sudo ip addr
echo "========================================================================"
echo "$CON1 namespace - ip addr"
echo "========================================================================"
sudo ip netns exec $CON1 ip addr
echo "========================================================================"
echo "$CON2 namespace - ip addr"
echo "========================================================================"
sudo ip netns exec $CON2 ip addr

echo "========================================================================"
echo "node - route table"
echo "========================================================================"
sudo route
echo
echo "========================================================================"
echo "$CON1 namespace - route table"
echo "========================================================================"
sudo ip netns exec $CON1 route
echo
echo "========================================================================"
echo "$CON2 namespace - route table"
echo "========================================================================"
sudo ip netns exec $CON2 route
