#!/bin/bash -e 

. env.sh

echo "========================================================================"
echo "Namespaces"
echo "========================================================================"
ip netns list
echo
echo "========================================================================"
echo "node - ip addr"
echo "========================================================================"
sudo ip addr
echo
echo "========================================================================"
echo "$CON1 namespace - ip addr"
echo "========================================================================"
sudo ip netns exec $CON1 ip addr
echo
echo "========================================================================"
echo "$CON2 namespace - ip addr"
echo "========================================================================"
sudo ip netns exec $CON2 ip addr
echo
echo "========================================================================"
echo "node - ip route"
echo "========================================================================"
sudo ip route
echo
echo "========================================================================"
echo "$CON1 namespace - ip route"
echo "========================================================================"
sudo ip netns exec $CON1 ip route
echo
echo "========================================================================"
echo "$CON2 namespace - ip route"
echo "========================================================================"
sudo ip netns exec $CON2 ip route
echo
